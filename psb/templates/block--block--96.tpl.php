<h1>For the public</h1>
<h6>The Professional Standards Board for Patent and Trade Marks Attorneys (PSB) is a statutory body responsible for upholding the standards and regimes required by all registered trade marks attorneys and patent attorneys working in Australia. It is responsible for investigating and prosecuting complaints against registered attorneys and can act on information from any source, including a complaint from a member of the public.</h6>
